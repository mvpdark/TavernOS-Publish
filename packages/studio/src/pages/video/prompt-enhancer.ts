/** 从数组中随机选取一个元素 */
function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]!;
}

/** 从数组中随机选取 n 个不重复元素 */
function pickMultiple<T>(arr: T[], count: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, arr.length));
}

/**
 * 基于关键词评分自动检测场景类型
 * 遍历所有场景的关键词列表，统计命中数，返回得分最高的场景
 */
function detectSceneType(input: string): SceneType {
  const scores: Record<SceneType, number> = {
    combat: 0,
    emotion: 0,
    comedy: 0,
    landscape: 0,
    cinematic: 0,
    product: 0,
    aesthetic: 0,
  };

  for (const [sceneKey, preset] of Object.entries(SCENE_PRESETS) as [SceneType, ScenePreset][]) {
    for (const kw of preset.keywords) {
      if (input.includes(kw)) {
        scores[sceneKey]++;
      }
    }
  }

  // 选出得分最高的场景；平局时按优先级顺序
  const priority: SceneType[] = [
    "combat", "emotion", "comedy", "cinematic",
    "aesthetic", "product", "landscape",
  ];
  let bestScene: SceneType = "landscape";
  let bestScore = 0;
  for (const scene of priority) {
    if (scores[scene] > bestScore) {
      bestScore = scores[scene];
      bestScene = scene;
    }
  }
  // 如果所有得分都是0，默认为 landscape（最通用）
  if (bestScore === 0) {
    bestScene = "cinematic";
  }
  return bestScene;
}

/** 从用户输入中提取核心主体描述 */
function extractCore(input: string): string {
  const trimmed = input.trim();
  if (trimmed.length > 80) return trimmed;
  return trimmed;
}

