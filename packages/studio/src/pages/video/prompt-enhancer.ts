/**
 * Seedance 2.0 通用多场景 Prompt 增强器
 *
 * 基于万能公式：主体 + 动作 + 场景 + 光影 + 镜头 + 风格 + 画质 + 约束
 * 支持7大场景类型自动检测与手动指定，每次调用随机组合产生不同结果。
 *
 * 场景类型：
 * - combat:    打斗/动作（武侠、枪战、科幻、奇幻、动漫格斗）
 * - emotion:   情感/剧情（拥抱、告别、重逢、反转、虐恋、暗恋等短剧场景）
 * - comedy:    搞笑/日常（滑倒、搞怪、动物搞笑、沙雕日常、表情包场景）
 * - landscape: 风景/治愈（日落、晨雾、樱花、星空、雨景、城市夜景、海边、秋日落叶）
 * - cinematic: 电影特效/创意（时空穿越、变身、巨物恐惧、超现实、水墨国风、赛博朋克追车、毒液变身）
 * - product:   产品/电商（美妆特写、美食诱惑、科技开箱、服装展示、饮品气泡、珠宝微距）
 * - aesthetic: 古风唯美（樱花雨、水墨、古风独舞、仙侠御剑、国风特效、红衣女侠）
 *
 * 纯 TypeScript 实现，无外部依赖。
 */

// ============================================================================
// 类型定义
// ============================================================================

/** 所有支持的场景类型 */
export type SceneType =
  | "combat"
  | "emotion"
  | "comedy"
  | "landscape"
  | "cinematic"
  | "product"
  | "aesthetic";

/** 场景预设数据结构 */
export interface ScenePreset {
  /** UI显示标签 */
  label: string;
  /** UI图标（emoji或字符） */
  icon: string;
  /** 场景检测关键词（用于自动识别） */
  keywords: string[];
  /** 场景开场/氛围池 */
  openings: string[];
  /** 动作/事件池 */
  actions: string[];
  /** 镜头语言池 */
  cameras: string[];
  /** 光影/色调池 */
  lightings: string[];
  /** 特效/细节池 */
  effects: string[];
  /** 情绪池 */
  moods: string[];
  /** 画质词池（场景专属，会与通用池合并） */
  qualityWords: string[];
  /** 收尾/结句池 */
  endings: string[];
}

/** 增强选项 */
export interface EnhanceOptions {
  /** 视频时长（秒），影响编排节奏，默认8秒 */
  duration?: number;
  /** 手动指定场景类型；"auto"表示自动检测（默认） */
  sceneType?: SceneType | "auto";
}

/** 增强结果 */
export interface EnhanceResult {
  /** 增强后的完整 prompt */
  enhanced: string;
  /** 检测到或使用的场景类型 */
  detectedScene: SceneType;
  /** 场景中文标签 */
  sceneLabel: string;
}

